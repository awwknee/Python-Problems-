print("Zero: {:d}".format(4 + 4 - 4 - 4))
print("One: {:.0f}".format((4 / 4) * (4 / 4)))
print("Two: {:.0f}".format((4 / 4) + (4 / 4)))
print("Three: {:.0f}".format((4 + 4 + 4) / 4))
print("Four: {:.0f}".format(4 + (4 - 4) * 4))
print("Five: {:.0f}".format((4 + 4 * 4) / 4))
print("Six: {:.0f}".format(4 + (4 + 4) / 4))
print("Seven: {:.0f}".format((4 + 4) - (4 / 4)))
print("Eight: {:.0f}".format((4 + 4) - (4 - 4)))
print("Nine: {:.0f}".format(4 + 4 + (4 / 4)))
